    private void btnEnviarMensajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarMensajeActionPerformed
        JFileChooser selector=new JFileChooser();
        selector.showOpenDialog(this);

        File archivo = selector.getSelectedFile();

        if (archivo != null) {
            String Origen = archivo.getPath();
            ImageIcon ingresar = new ImageIcon(Origen);
            Icon iconoIngresar = new ImageIcon(ingresar.getImage().getScaledInstance(labelImagen.getWidth(), labelImagen.getHeight(), Image.SCALE_FAST));
            labelImagen.setIcon(iconoIngresar);
            txtRutaImagen.setText(Origen);
            
        }
        else {
            JOptionPane.showMessageDialog(null, "Por favor seleccione una imagen");
        }
    }//GEN-LAST:event_btnEnviarMensajeActionPerformed

